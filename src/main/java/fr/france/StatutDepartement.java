package fr.france;

/**
 * <b>StatutDepartement est l'énumérateur représentant les différents statut de
 * département français.</b>
 * 
 * @see Departement
 * 
 * @author MathieuSoysal
 * @version 1.0.0
 */
public enum StatutDepartement {
    /**
     * Collectivités d'outre-mer
     */
    COM,
    /**
     * Départements d'outre-mer
     */
    DOM,
    /**
     * statut Départemental
     */
    DPT,
    /**
     * Pays et territoires d'outre-mer
     */
    PTOM,
    /**
     * Territoires d'outre-mer
     */
    TOM
}
